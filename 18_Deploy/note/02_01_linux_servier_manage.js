//              기본 정보
//              PID : 프로세스 아이디
//              USER : 프로세스를 실행시킨 사용자 (권한)

//              * 프로세스를 강제로 종료하려면
`                   kill -9 $(PID)
`//                 PID : 프로세스 아이디

//              우선순위 관련 정보
//              PR : Priority, 프로세스 우선순위, 낮을수록 우선순위 높음
//              NI : Nice Value, PR을 조정하는 값, 낮을수록 PR을 떨어뜨림

//              메모리 점유율 관련 정보
//              VIRT : Virtual, 가상 메모리 점유율, 미리 넉넉하게 할당받은 메모리의 양
//              RES : Resident, 물리적인 메모리 점유율 => 핵심
//              SHR : Share, 공유 메모리 점유율, 다른 프로세스와 공유하는 메모리

//              S : 프로세스의 현재 상태
//                  S : Sleep, 자는중
//                  I : Idle, 휴식중
//                  R : Running, 실행중

//              리소스 사용량 관련 정보
//              %CPU : CPU 점유율, %로 나옴
//              %MEM : MEMORY 점유율, %로 나옴

//              TIME+ : 프로세스 실행 시간
//              COMMAND : 프로세스 실행 명령어

