//              top - 08:38:11 up 1:48,
//              : 명령어와 실행 시간

//              1 users,
//              : 1명의 사용자가 SSH 실행중

//              load average: 0.01, 0.02, 0.01,
//              : 부하 평균 (1분, 5분, 15분 평균)

//              Tasks:  115 total,  1 running,  114 sleeping,  0 stopped,  0 zombie
//              : 작업중인 프로세스 개수, 실행중인, 자는중인, 중단된, 좀비(멈춤)

//              %Cpu(s):  0.3 us,  0.2 sy,  0.0 ni, 99.5 id,  0.0 wa, ... (뒤에는 안중요)
//              : CPU 사용량, us(유저), sy(시스템), ni(Nice, 우선순위 스케줄러), id(Idle), wa(IO 웨이팅)

//              MiB Mem :   3903.9 total,   2158.8 free,   1098.2 used,    647.0 buff/cache
//              : 메모리 사용량, total(전체), free(사용가능), used(사용중), buff/cache(캐싱된것, 지울수있음)

//              MiB Swap:      0.0 total,      0.0 free,      0.0 used.   2566.0 avail Mem
//              : 스왑(디스크를 메모리로 사용), 지금은 사용안함으로 설정되어 있어서 0으로 나옴
//                메모리 공간이 꽉 차면 잘 사용하지 않는 것들을 디스크로 내릴 수 있는 설정


